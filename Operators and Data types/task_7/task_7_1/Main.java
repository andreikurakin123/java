int main() {
    // Бесконечный цикл while
    cout << "Бесконечный цикл while:" << endl;
    while (true) {
        cout << "Этот текст будет повторяться вечно!" << endl;
    }
    
    // Бесконечный цикл do-while
    cout << "\nБесконечный цикл do-while:" << endl;
    do {
        cout << "И этот текст тоже!" << endl;
    } while (true);
    
    return 0;
}